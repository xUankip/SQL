DROP TABLE PhongBan
CREATE TABLE PhongBan (
    MaPB VARCHAR (7) PRIMARY KEY,
    TenPB NVARCHAR (50)
)
GO
DROP TABLE NhanVien
GO
CREATE TABLE NhanVien (
    MaNV VARCHAR(7) PRIMARY KEY,
    TenNV NVARCHAR(50),
    NgaySinh DATETIME,
    SoCCCD CHAR (19),
    GioiTinh CHAR (1),
    DiaChi NVARCHAR (100),
    NgayVaoLam DATETIME,
    PhongBan1 VARCHAR(7),
    FOREIGN KEY (PhongBan1) REFERENCES PhongBan(MaPB)
)
GO
DROP TABLE LuongDA
CREATE TABLE LuongDA (
    MaDA VARCHAR (8),
    NgayNhan DATETIME,
    SoTien Money,
    MaNV VARCHAR(7),
    PRIMARY KEY (MaDA, MaNV),
    FOREIGN KEY (MaNV) REFERENCES NhanVien(MaNV)
)
GO
